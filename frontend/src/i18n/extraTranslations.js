function normalize(value) {
  return String(value || "")
    .replace(/\u00a0/g, " ")
    .replace(/\u2026/g, "...")
    .replace(/\s+/g, " ")
    .trim();
}

function makeMap(entries) {
  return new Map(entries.map(([english, translated]) => [normalize(english), translated]));
}

