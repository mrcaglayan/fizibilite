const maps = { es: spanish, fr: french };

export function translateAdditionalPhrase(english, language) {
  if (language !== "es" && language !== "fr") return null;
  const normalized = normalize(english);
  const exact = maps[language]?.get(normalized);
  if (exact) return exact;

  const fallbackRules = language === "es"
    ? [
        [/^Failed to load (.+)$/i, (_m, item) => `No se pudo cargar ${item}`],
        [/^Failed to create (.+)$/i, (_m, item) => `No se pudo crear ${item}`],
        [/^Failed to update (.+)$/i, (_m, item) => `No se pudo actualizar ${item}`],
        [/^Failed to save (.+)$/i, (_m, item) => `No se pudo guardar ${item}`],
        [/^No (.+) found\.$/i, (_m, item) => `No se encontro ${item}.`],
        [/^Search (.+)$/i, (_m, item) => `Buscar ${item}`],
      ]
    : [
        [/^Failed to load (.+)$/i, (_m, item) => `Echec du chargement de ${item}`],
        [/^Failed to create (.+)$/i, (_m, item) => `Echec de la creation de ${item}`],
        [/^Failed to update (.+)$/i, (_m, item) => `Echec de la mise a jour de ${item}`],
        [/^Failed to save (.+)$/i, (_m, item) => `Echec de l'enregistrement de ${item}`],
        [/^No (.+) found\.$/i, (_m, item) => `Aucun ${item} trouve.`],
        [/^Search (.+)$/i, (_m, item) => `Rechercher ${item}`],
      ];

  for (const [pattern, formatter] of fallbackRules) {
    const match = normalized.match(pattern);
    if (match) return formatter(...match);
  }
  return null;
}

export function dynamicText(language, tr, en, es, fr) {
  if (language === "tr") return tr;
  if (language === "es") return es;
  if (language === "fr") return fr;
  return en;
}
