function relativeTimeText(language, value) {
  const text = String(value || "").trim();
  if (/^(az once|az önce|just now|hace un momento|a l'instant)$/i.test(text)) {
    return dynamicText(language, "az once", "just now", "hace un momento", "a l'instant");
  }

  let match = text.match(/^(\d+) dk once$/i) || text.match(/^(\d+) min ago$/i) || text.match(/^hace (\d+) min$/i) || text.match(/^il y a (\d+) min$/i);
  if (match) {
    return dynamicText(language, `${match[1]} dk once`, `${match[1]} min ago`, `hace ${match[1]} min`, `il y a ${match[1]} min`);
  }

  match = text.match(/^(\d+) saat once$/i) || text.match(/^(\d+) h ago$/i) || text.match(/^hace (\d+) h$/i) || text.match(/^il y a (\d+) h$/i);
  if (match) {
    return dynamicText(language, `${match[1]} saat once`, `${match[1]} h ago`, `hace ${match[1]} h`, `il y a ${match[1]} h`);
  }

  return translatePhrase(text, language);
}

