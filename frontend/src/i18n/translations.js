export function normalizeKey(value) {
  return String(value || "")
    .replace(/\u00a0/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function foldTurkish(value) {
  return String(value || "")
    .replace(/Ç/g, "C")
    .replace(/Ğ/g, "G")
    .replace(/İ/g, "I")
    .replace(/Ö/g, "O")
    .replace(/Ş/g, "S")
    .replace(/Ü/g, "U")
    .replace(/ç/g, "c")
    .replace(/ğ/g, "g")
    .replace(/ı/g, "i")
    .replace(/ö/g, "o")
    .replace(/ş/g, "s")
    .replace(/ü/g, "u");
}

function preserveEdgeWhitespace(original, translated) {
  const source = String(original || "");
  const leading = source.match(/^\s*/)?.[0] || "";
  const trailing = source.match(/\s*$/)?.[0] || "";
  return `${leading}${translated}${trailing}`;
}

export function translatePhrase(value, language) {
  if (!value || !language) return value;
  const normalized = normalizeKey(value);
  if (!normalized) return value;
  const folded = normalizeKey(foldTurkish(normalized));
  const direct = phraseMap.get(normalized) || phraseMap.get(folded);
  if (direct?.[language]) {
    const translated = language === "tr" ? polishTurkish(direct[language]) : direct[language];
    return preserveEdgeWhitespace(value, translated);
  }
  if (/[•â€¢]/.test(normalized)) {
    const parts = normalized.split(/\s*(?:•|â€¢)\s*/).filter(Boolean);
    const translatedParts = parts.map((part) => translatePhrase(part, language));
    if (translatedParts.some((part, index) => part !== parts[index])) {
      const translated = translatedParts.join(" • ");
      return preserveEdgeWhitespace(value, language === "tr" ? polishTurkish(translated) : translated);
    }
  }
  for (const [pattern, formatter] of replacements) {
    const match = normalized.match(pattern) || folded.match(pattern);
    if (match) {
      const translated = formatter(...match, language);
      return preserveEdgeWhitespace(value, language === "tr" ? polishTurkish(translated) : translated);
    }
  }
  return value;
}
